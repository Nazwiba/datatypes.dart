void main() {
  String size = "XXXL";
  if (size == "XS") {
    print("Shirt size is XS");
  } else if (size == "S") {
    print("Shirt size is S");
  } else if (size == "M") {
    print("Shirt size is M");
  } else if (size == "L") {
    print("Shirt size is L");
  } else if (size == "XL") {
    print("Shirt size XL");
  } else if (size == "XXL") {
    print("Shirt size XXL");
  } else {
    print("Shirt size is not available");
  }
}
